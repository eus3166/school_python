"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 5: 시험 합격

필기 점수와 실기 점수를 입력받아 합격 여부를 출력하세요.
중첩 if문을 사용합니다.

[판정 기준]
- 필기 점수가 60점 이상이면(필기 합격):
  - 실기 점수가 70점 이상이면: "최종 합격입니다!"
  - 실기 점수가 70점 미만이면: "실기 불합격입니다. 실기 재시험 대상입니다."
- 필기 점수가 60점 미만이면(필기 불합격):
  "필기 불합격입니다. 필기부터 다시 응시하세요."
"""

written = int(input("필기 점수를 입력하세요: "))

# 아래에 중첩 if문을 작성하세요
# 필기 합격일 때만 실기 점수를 입력받으세요

# 필기 60점 이상이면 "필기 합격!"과 실기 점수를 추가로 입력 받음
if written >= 60:
    print("필기 합격!")
    practice = int(input("실기 점수를 입력하세요: "))
#   실기 70점 이상이면 "최종 합격입니다!"
    if practice >= 70:
        print("최종 합격입니다!")
#   그 외는 "실기 불합격입니다. 실기 재시험 대상입니다."
    else:
        print("실기 불합격입니다. 실기 재시험 대상입니다.")

# 그 외는 "필기 불합격입니다. 필기부터 다시 응시하세요."
else:
    print("필기 불합격입니다. 필기부터 다시 응시하세요.")

