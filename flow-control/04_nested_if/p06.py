"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 6: 놀이기구 탑승

키를 입력받아 놀이기구 탑승 가능 여부를 출력하세요.
중첩 if문을 사용합니다.

[판정 기준]
- 키가 130cm 이상이면:
  - 보호자 동반(Y): "보호자와 함께 탑승 가능합니다."
  - 보호자 미동반(N):
    - 키가 150cm 이상이면: "혼자 탑승 가능합니다."
    - 키가 150cm 미만이면: "보호자가 필요합니다."
- 키가 130cm 미만이면: "탑승할 수 없습니다."
"""

height = int(input("키를 입력하세요(cm): "))

# 아래에 중첩 if문을 작성하세요

# 키가 130cm 이상이면
if height >= 130:
    # 보호자 동반 여부를 입력 받음
    guardian = input("보호자가 동반하나요? (Y/N): ")
    # 보호자가 있으면 "보호자와 함께 탑승 가능합니다."
    if guardian == "Y":
        print("보호자와 함께 탑승 가능합니다.")
    # 그 외는
    else: 
        # 키가 150cm 이상이면 "혼자 탑승 가능합니다."
        if height >= 150:
            print("혼자 탑승 가능합니다.")
        # 그 외는 "보호자가 필요합니다."
        else:
            print("보호자가 필요합니다.")
# 그 외는 "탑승할 수 없습니다."
else:
    print("탑승할 수 없습니다.")
