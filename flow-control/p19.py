"""
실습 19: 수료 판정

점수와 출석률을 입력받아,
점수가 80점 이상이고 출석률이 90을 초과하면 "수료 가능"을,
그렇지 않으면 "수료 불가"를 출력하세요.

힌트: 비교 연산자(>=, >)와 논리 연산자(and)를 조합합니다.

참고: 조건에 따라 다른 결과를 출력하려면 if/else를 사용합니다.
      if 조건:
          참일 때 실행
      else:
          거짓일 때 실행
"""

score = int(input("점수를 입력하세요: "))
attendance = int(input("출석률을 입력하세요: "))

# 아래에 수료 여부를 판별하여 출력하세요

# 만약 점수가 80점 이상이고 출석률이 90을 초과하면 "수료 가능"
if score >= 80 and attendance > 90 :
    print("수료 가능")

# 그렇지 않으면 "수료 불가"
else:
    print("수료 불가")
