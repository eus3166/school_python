"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 9: 혈액형 성격

혈액형(A, B, O, AB)을 입력받아 성격 설명을 출력하세요.
if/elif/else를 사용합니다.

[성격 설명]
A형: 꼼꼼하고 신중한 성격
B형: 자유롭고 창의적인 성격
O형: 사교적이고 리더십이 강한 성격
AB형: 이성적이고 독창적인 성격
그 외: "잘못된 입력입니다."

주의: AB형을 먼저 검사해야 합니다! (왜 그런지 생각해보세요)
"""

blood = input("혈액형을 입력하세요 (A/B/O/AB): ")

# 아래에 성격 설명을 출력하세요
# 만약 A를 먼저 검사하게 되면 "AB" 입력 시에도 A로 판정될 수도 있다.

# 만약 AB형이면 "이성적이고 족창적인 성격"
if blood == "AB":
    personality = "이성적이고 족창적인 성격"

# 그게 아니고 A형이면 "꼼꼼하고 신중한 성격"
elif blood == "A":
    personality = "꼼꼼하고 신중한 성격"

# 그게 아니고 B형이면 "자유롭고 창의적인 성격"
elif blood == "B":
    personality = "자유롭고 창의적인 성격"

# 그게 아니고 O형이면 "사교적이고 리더십이 강한 성격"
elif blood == "O":
    personality = "사교적이고 리더십이 강한 성격"

# 그 외의 나머지는 ""
else:
    personality = ""

# 만약 personality != ""이라면 혈액형과 성격을 출력한다.
if personality != "":
    print(f"혈액형: {blood}형")
    print(f"성격: {personality}")


# 그게 아니라면 "잘못된 입력입니다."를 출력한다.
else:
    print("잘못된 입력입니다.")
