"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 8: 온도별 옷차림 추천

기온(°C)을 입력받아 적절한 옷차림을 추천하세요.
if/elif/else를 사용합니다.

[추천 기준]
5도 미만: 패딩, 두꺼운 목도리
5도 이상 10도 미만: 코트, 니트
10도 이상 20도 미만: 자켓, 가디건
20도 이상 28도 미만: 반팔, 얇은 셔츠
28도 이상: 민소매, 반바지
"""

temp = float(input("현재 기온을 입력하세요 (°C): "))

# 아래에 옷차림을 추천하여 출력하세요

# 만약에 5도 미만이면 "패딩, 두꺼운 목도리"
if temp < 5:
    clothes = "패딩, 두꺼운 목도리"

# 그게 아니고 5도 이상 10도 미만이면 "코트, 니트"
elif temp < 10:
    clothes = "코트, 니트"

# 그게 아니고 10도 이상 20도 미만이면 "자켓, 가디건"
elif temp < 20:
    clothes = "자켓, 가디건"

# 그게 아니고 20도 이상 28도 미만이면 "반팔, 얇은 셔츠"
elif temp < 28:
    clothes = "반팔, 얇은 셔츠"

# 이외의 나머지는 "민소매, 반바지"
else:
    clothes = "민소매, 반바지" 

# 현재 기온이 몇 도인지 알려준다.
print(f"현재 기온: {temp}°C")

# 추천 옷차림을 출력한다.
print(f"추천 옷차림: {clothes}")
