"""
실습 22: 이벤트 참여 판정

회원 여부, 나이, VIP 여부를 입력받아,
회원이고, 나이가 20세 이상이거나 VIP이면 "이벤트 참여 가능"을,
그렇지 않으면 "이벤트 참여 불가"를 출력하세요.

힌트: and, or를 조합합니다.
      or 조건을 괄호로 묶어야 의도한 대로 동작합니다.

참고: if 조건:
          참일 때 실행
      else:
          거짓일 때 실행
"""

is_member = input("회원 여부 (y/n): ")
age = int(input("나이를 입력하세요: "))
is_vip = input("VIP 여부 (y/n): ")

# 아래에 이벤트 참여 가능 여부를 판별하여 출력하세요

# 만약 회원이고 (나이가 20세 이상이거나 vip이면) "이벤트 참여 가능"
if is_member == "y" and (age >= 20 or is_vip == "y"):
    print("이벤트 참여 가능")

# 그게 아니면 "이벤트 참여 불가"
else:
    print("이벤트 참여 불가")
