"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 3: 피자 주문 처리 프로그램

주문 방식(배달/포장)을 입력받아 처리 절차를 출력하세요.

[지시사항]
1. 주문 방식을 input()으로 입력받으세요
2. "배달"이면 배달 절차를, 아니면 포장 절차를 출력하세요
3. 어떤 경우든 마지막에 "주문 완료!"를 출력하세요
"""

# 아래에 코드를 작성하세요

# 주문 방식(배달 또는 포장)을 입력 받는다.
order = input("주문 방식 (배달/포장): ")

# 만약 배달이라면 "배달 주소를 확인합니다"와 "라이더를 배정합니다"를 출력한다.
if order == "배달":
    print("배달 주소를 확인합니다")
    print("라이더를 배정합니다")

# 그것이 아니라면 "포장 용기에 담습니다"와 "수령 대기합니다"를 출력한다.
else:
    print("포장 용기에 담습니다")
    print("수령 대기합니다")

# 어떤 경우든 마지막에는 "주문 완료!"를 출력한다.
print("주문 완료!")

