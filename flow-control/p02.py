"""
실습 2: 성적 등급

점수를 입력받아 등급을 출력하세요.
if/elif/else를 사용합니다.

[등급 기준]
90 이상: A
80 이상: B
70 이상: C
60 이상: D
60 미만: F
"""

score = int(input("점수를 입력하세요: "))

# 아래에 등급을 판정하여 출력하세요

# 만약 90점 이상이면 "등급: A"를 출력하세요
if score >= 90:
    print("등급: A")

# 그게 아니고 80점 이상이면 "등급: B"
elif score >= 80:
    print("등급: B")

# 그게 아니고 70점 이상이면 "등급: C"
elif score >= 70:
    print("등급: C")

# 그게 아니고 60점 이상이면 "등급: D"
elif score >= 60:
    print("등급: D")

# 그게 아니고 60점 미만이면 "등급: F"
elif score < 60:
    print("등급: F")
