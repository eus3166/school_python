"""
실습 16: 파일 크기 표시 (삼항 연산자)

파일 크기(바이트)를 입력받아
1024 이상이면 KB 단위로, 아니면 B 단위로 출력하세요.
삼항 연산자를 활용합니다.
"""

size = int(input("파일 크기(바이트) 입력: "))

# 아래에 삼항 연산자로 코드를 작성하세요

# 만약 파일 크기가 1024 이상이면 KB 단위 나타내고 그게 아니면 B 단위로 나타낸다.
result = f"{size / 1024:.1f}KB" if size >= 1024 else f"{size}B"

# 파일 크기를 출력한다.
print(f"파일 크기: {result}")
