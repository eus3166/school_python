"""
실습 3: 문자열 꾸미기

이름과 메시지를 입력받아 아래와 같은 형태로 출력하세요.
문자열 연산자(+, *)를 활용합니다.
"""

name = input("이름: ")
message = input("메시지: ")

# 아래에 코드를 작성하세요

# *를 20번 출력한다.
print("*"*20)

# 입력 받은 이름을 "*", name,"님의 한마디*"의 형태로 출력한다.
print(f"*{name}의 한마디*")

# 입력 받은 메시지를 "*", message, "*" 형태로 출력한다.
print(f"*{message}*")

# *를 20번 출력한다.
print("*"*20)
